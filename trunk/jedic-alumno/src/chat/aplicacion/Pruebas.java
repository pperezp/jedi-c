private void txtMsnKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMsnKeyReleased
        
    if(txtMsn.getText().contains("xD")){
            try {
                SimpleAttributeSet at = new SimpleAttributeSet();
                System.out.println(txtMsn.getText().length());
                txtMsn.getStyledDocument().remove(txtMsn.getText().length()-2, 2);
                StyleConstants.setBold(at, true);
                StyleConstants.setItalic(at, true);
                StyleConstants.setFontFamily(at, Font.MONOSPACED);
                StyleConstants.setFontSize(at, 22);
                StyleConstants.setForeground(at, Color.red);
                txtMsn.insertIcon(new ImageIcon(this.getClass().getResource("xD.gif")));

                txtMsn.setCharacterAttributes(at, true);
                txtMsn.repaint();
            } catch (BadLocationException ex) {
//                Logger.getLogger(Pruebas.class.getName()).log(Level.SEVERE, null, ex);
            }
            
    }
}//GEN-LAST:event_txtMsnKeyReleased

