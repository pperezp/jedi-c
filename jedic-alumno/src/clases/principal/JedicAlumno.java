    private void accionCerrarPestaña() {
        index = tabconte.getSelectedIndex();
        if (codigos.get(index).codigoGuardado()) {
            removerTab(index);
        } else {
            if ((Mensaje.OPCION = Mensaje.preguntaSiNoCancelar("El código actual \"" + codigos.get(index).getNombre() + "\" aún no se "
                    + "ha guardado, ¿Desea Guardar antes de cerrar la pestaña?")) == Mensaje.SI) {
                if (codigos.get(index).getPathConExtension() == null) {
                    guardarComo();
                } else {
                    codigos.get(index).guardar(m_guardar);
                }
            }
            if (Mensaje.OPCION != Mensaje.CANCELAR) {
                removerTab(index);
            }
        }

    }

    private void cerrarTodasLasPestañas() {
        if (tabconte.getTabCount() != 0) {
            while (!codigos.isEmpty()) {
                if (tabconte.getSelectedIndex() != 0) {
                    if (!(codigos.get(0).codigoGuardado())) {
                        tabconte.setSelectedComponent(scrolls.get(0));
                        if ((Mensaje.OPCION = Mensaje.preguntaSiNoCancelar("La Codificación \"" + codigos.get(0).getNombre() + "\" aún no se "
                                + "ha guardado, ¿Desea Guardarla antes de Salir?")) == Mensaje.SI) {
                            if (codigos.get(0).getPathConExtension() == null) {
                                guardarComo();
                            } else {
                                codigos.get(0).guardar(m_guardar);
                            }
                            removerTab(0);
                        } else if (Mensaje.OPCION == Mensaje.NO) {
                            removerTab(0);
                        }
                        if (Mensaje.OPCION == Mensaje.CANCELAR) {
                            break;
                        }
                    } else {
                        removerTab(0);
                    }
                }
            }
        }
    }

    private void opcionCerrarPestaña() {
        if (tabconte.getTabCount() != 0) {
            accionCerrarPestaña();
        }
    }

